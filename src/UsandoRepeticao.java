public class UsandoRepeticao {
    public static void main(String[] args) {
        int contador = 1;
        while (contador <= 10) {
            System.out.println(contador);
        contador += 1;
    }
        System.out.println(contador);

        for (int i = 1; i < 10 ; i++) {
            System.out.println(i);
        }
    }
}
